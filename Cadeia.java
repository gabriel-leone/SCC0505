public class Cadeia {
    public String cadeia;

    public Cadeia(String cadeia) {
        this.cadeia = cadeia;
    }

    public void Cadeira() {
        this.cadeia = "";
    }

    @Override
    public String toString() {
        return "Cadeia{" +
                "cadeia='" + cadeia + '\'' +
                '}';
    }
}
